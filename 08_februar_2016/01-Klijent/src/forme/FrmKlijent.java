    private void srediFormu() {
        try {
            this.setTitle("[FON] Izmena racuna - klijent");
            listaZaBrisanje = new ArrayList<>();
            listaZaCuvanje = new ArrayList<>();

            jtxtSifra.setText("");
            jtxtDatum.setText("");

            RacunTableModel model = new RacunTableModel(new Racun());
            jtblStavke.setModel(model);

            JComboBox jcbProizvod = new JComboBox();
            TableColumn tcProizvod = jtblStavke.getColumnModel().getColumn(0);
            tcProizvod.setCellEditor(new DefaultCellEditor(jcbProizvod));

            TransferObjekatZahtev toz1 = new TransferObjekatZahtev();
            toz1.setOperacija(Konst.VRATI_KLIJENTE);
            Komunikacija.getInstance().posaljiZahtev(toz1);
            TransferObjekatOdgovor too1 = Komunikacija.getInstance().primiOdgovor();
            List<Klijent> listaK = (List<Klijent>) too1.getRezultat();

            jcbKlijent.setModel(new DefaultComboBoxModel(listaK.toArray()));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Neuspesna inicijalizacija forme i njenih komponenti!");
            ex.printStackTrace();
        }
    }

