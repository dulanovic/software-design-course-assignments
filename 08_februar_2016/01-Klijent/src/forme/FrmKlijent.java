    private void jbtnPronadjiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPronadjiActionPerformed
        try {
            int id = Integer.parseInt(jtxtSifra.getText());

            TransferObjekatZahtev toz1 = new TransferObjekatZahtev();
            toz1.setOperacija(Konst.NADJI_RACUN);
            toz1.setParametar(id);
            Komunikacija.getInstance().posaljiZahtev(toz1);
            TransferObjekatOdgovor too1 = Komunikacija.getInstance().primiOdgovor();
            Racun racun = (Racun) too1.getRezultat();
            this.setR(racun);

            jtxtSifra.setText(String.valueOf(racun.getRacunID()));
            jtxtDatum.setText(df.format(racun.getDatum()));
            jcbKlijent.setSelectedItem(racun.getKlijent());

            RacunTableModel model = new RacunTableModel(racun);
            jtblStavke.setModel(model);

            JComboBox jcbProizvod = new JComboBox();
            TableColumn tcProizvod = jtblStavke.getColumnModel().getColumn(0);
            tcProizvod.setCellEditor(new DefaultCellEditor(jcbProizvod));

            TransferObjekatZahtev toz2 = new TransferObjekatZahtev();
            toz2.setOperacija(Konst.VRATI_PROIZVODE);
            Komunikacija.getInstance().posaljiZahtev(toz2);
            TransferObjekatOdgovor too2 = Komunikacija.getInstance().primiOdgovor();
            List<Proizvod> listaP = (List<Proizvod>) too2.getRezultat();

            jcbProizvod.setModel(new DefaultComboBoxModel(listaP.toArray()));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Neuspesno ucitavanje racuna na formu preko unete sifre!");
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jbtnPronadjiActionPerformed

    private void jbtnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDodajActionPerformed
        if (this.r == null) {
            JOptionPane.showMessageDialog(this, "Nije unet racun, prema toma nemoguce je uneti stavke racuna!");
            return;
        }

        RacunTableModel model = (RacunTableModel) jtblStavke.getModel();
        StavkaRacuna sr = new StavkaRacuna();
        model.dodajStavku(sr);
        this.listaZaCuvanje.add(sr);
    }//GEN-LAST:event_jbtnDodajActionPerformed

    private void jbtnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnObrisiActionPerformed
        int red = jtblStavke.getSelectedRow();
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Niste odabrali red!");
        }
        RacunTableModel model = (RacunTableModel) jtblStavke.getModel();
        StavkaRacuna sr = model.obrisiStavku(red);
        this.getListaZaBrisanje().add(sr);
    }//GEN-LAST:event_jbtnObrisiActionPerformed

    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        try {
            int racunID = Integer.parseInt(jtxtSifra.getText());
            Date datum = df.parse(jtxtDatum.getText());
            Klijent k = (Klijent) jcbKlijent.getSelectedItem();

            Racun racun = new Racun(racunID, datum, k);

            RacunTableModel model = (RacunTableModel) jtblStavke.getModel();
            racun.setLista(model.vratiListu());
            racun.setListaZaBrisanje(this.getListaZaBrisanje());
            racun.setListaZaCuvanje(listaZaCuvanje);

            TransferObjekatZahtev toz = new TransferObjekatZahtev();
            toz.setOperacija(Konst.SACUVAJ_RACUN);
            toz.setParametar(racun);
            Komunikacija.getInstance().posaljiZahtev(toz);

            JOptionPane.showMessageDialog(this, "Racun je uspesno sacuvan!");
            this.setR(null);
            srediFormu();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Neuspesno cuvanje racuna!");
        }

    }//GEN-LAST:event_jbtnSacuvajActionPerformed

