    private void srediFormu() {
        try {
            this.setTitle("[FON] Klijent");

            StavkeTableModel model = new StavkeTableModel(new Racun());
            jtblStavke.setModel(model);

            JComboBox jcbKnjiga = new JComboBox();
            TableColumn tcKnjiga = jtblStavke.getColumnModel().getColumn(1);
            tcKnjiga.setCellEditor(new DefaultCellEditor(jcbKnjiga));

            TransferObjekatZahtev toz = new TransferObjekatZahtev();
            toz.setOperacija(Konst.VRATI_KNJIGE);
            Komunikacija.getInstance().posaljiZahtev(toz);
            TransferObjekatOdgovor too = Komunikacija.getInstance().primiOdgovor();
            List<Knjiga> listaKnjiga = (List<Knjiga>) too.getRezultat();

            jcbKnjiga.setModel(new DefaultComboBoxModel(listaKnjiga.toArray()));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Neuspesno ucitavanje listi na formu");
            ex.printStackTrace();
        }

    }

