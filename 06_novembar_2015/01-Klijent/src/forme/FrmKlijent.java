    private boolean izvrsiValidaciju() {
        StavkeTableModel model = (StavkeTableModel) jtblStavke.getModel();
        List<StavkaRacuna> lista = model.vratiListu();
        if (lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Lista nema nijednu stavku!");
            return false;
        }
        for (StavkaRacuna sr : lista) {
            if (sr.getKolicina() == 0) {
                JOptionPane.showMessageDialog(this, "Kolicina je 0!");
                return false;
            }
        }

        return true;
    }
