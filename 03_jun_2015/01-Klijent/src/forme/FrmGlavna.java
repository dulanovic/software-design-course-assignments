    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        try {
            if (!izvrsiValidaciju()) {
                return;
            }

            String naziv = jtxtNaziv.getText();
            Date datumOdrzavanja = df.parse(jtxtDatum.getText());
            Dizajner d = (Dizajner) jcbDizajner.getSelectedItem();

            ModnaRevija mr = new ModnaRevija(naziv, datumOdrzavanja, d);

            AngazovanjeTableModel model = (AngazovanjeTableModel) jtblAngazovanje.getModel();
            mr.setListaAngazovanja(model.vratiListu());

            TransferObjekatZahtev toz = new TransferObjekatZahtev();
            toz.setOperacija(Konstante.SACUVAJ_MODNU_REVIJU);
            toz.setParametar(mr);
            Komunikacija.getInstance().posaljiZahtev(toz);
            TransferObjekatOdgovor too = Komunikacija.getInstance().primiOdgovor();
            if (too.getIzuzetak() != null) {
                JOptionPane.showMessageDialog(this, too.getPoruka());
                return;
            }

            JOptionPane.showMessageDialog(this, "Uspesno sacuvana modna revija!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Nespesno sacuvana modna revija!");
            ex.printStackTrace();
        }

    }//GEN-LAST:event_jbtnSacuvajActionPerformed

