    private void srediFormu() {
        try {
            AngazovanjeTableModel model = new AngazovanjeTableModel(new ArrayList<Angazovanje>());
            jtblAngazovanje.setModel(model);

            TransferObjekatZahtev toz = new TransferObjekatZahtev();
            toz.setOperacija(Konstante.VRATI_DIZAJNERE);
            Komunikacija.getInstance().posaljiZahtev(toz);
            TransferObjekatOdgovor too = Komunikacija.getInstance().primiOdgovor();
            List<Dizajner> listaDizajnera = (List<Dizajner>) too.getRezultat();

            TransferObjekatZahtev toz1 = new TransferObjekatZahtev();
            toz1.setOperacija(Konstante.VRATI_MODELE);
            Komunikacija.getInstance().posaljiZahtev(toz1);
            TransferObjekatOdgovor too1 = Komunikacija.getInstance().primiOdgovor();
            List<Model> listaModela = (List<Model>) too1.getRezultat();

            JComboBox jcbModel = new JComboBox();
            TableColumn tcModel = jtblAngazovanje.getColumnModel().getColumn(0);
            tcModel.setCellEditor(new DefaultCellEditor(jcbModel));

            jcbModel.setModel(new DefaultComboBoxModel(listaModela.toArray()));
            jcbDizajner.setModel(new DefaultComboBoxModel(listaDizajnera.toArray()));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Neuspesno ucitavanje listi na formu!");
        }
    }

