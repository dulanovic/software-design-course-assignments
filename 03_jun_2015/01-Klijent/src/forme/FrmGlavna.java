    private boolean izvrsiValidaciju() {
        if (jtxtNaziv.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Niste uneli naziv modne revije!");
            return false;
        }

        if (jtxtDatum.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Niste uneli datum odrzavanja modne revije!");
            return false;
        }

        AngazovanjeTableModel model = (AngazovanjeTableModel) jtblAngazovanje.getModel();
        List<Angazovanje> lista = model.vratiListu();

        for (Angazovanje a : lista) {
            if (a.getModel() == null) {
                JOptionPane.showMessageDialog(this, "Niste izabrali model pri angazovanju!");
                return false;
            }
            if (a.getBrojSati() == 0) {
                JOptionPane.showMessageDialog(this, "Niste uneli broj sati kod angazovanja modela: " + a.getModel() + "!");
                return false;
            }
            if (a.getKomentar() == null) {
                JOptionPane.showMessageDialog(this, "Niste uneli komentar kod angazovanja modela: " + a.getModel() + "!");
                return false;
            }
        }

        return true;
    }
