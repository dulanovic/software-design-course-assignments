    private void srediFormu() {
        try {
            this.setTitle("[FON] Unos projekta");

            AngazovanjeTableModel model = new AngazovanjeTableModel(new Projekat());
            jtblAngazovanje.setModel(model);

            JComboBox jcbInzenjer = new JComboBox();
            TableColumn tcInzenjer = jtblAngazovanje.getColumnModel().getColumn(1);
            tcInzenjer.setCellEditor(new DefaultCellEditor(jcbInzenjer));

            TransferObjekatZahtev toz1 = new TransferObjekatZahtev();
            toz1.setOperacija(Konst.VRATI_VRSTE_PROJEKATA);
            Komunikacija.getInstance().posaljiZahtev(toz1);
            TransferObjekatOdgovor too1 = Komunikacija.getInstance().primiOdgovor();
            List<VrstaProjekta> listaVP = (List<VrstaProjekta>) too1.getRezultat();

            TransferObjekatZahtev toz2 = new TransferObjekatZahtev();
            toz2.setOperacija(Konst.VRATI_INZENJERE);
            Komunikacija.getInstance().posaljiZahtev(toz2);
            TransferObjekatOdgovor too2 = Komunikacija.getInstance().primiOdgovor();
            List<Inzenjer> listaI = (List<Inzenjer>) too2.getRezultat();

            jcbVrstaProjekta.setModel(new DefaultComboBoxModel(listaVP.toArray()));
            jcbInzenjer.setModel(new DefaultComboBoxModel(listaI.toArray()));

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Neuspesno ucitavanje listi na formu!");
        }
    }

