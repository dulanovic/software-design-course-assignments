    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        try {
            if (!izvrsiValidaciju()) {
                return;
            }

            String naziv = jtxtNaziv.getText();
            Date datumOd = df.parse(jtxtDatumOd.getText());
            Date datumDo = df.parse(jtxtDatumDo.getText());
            VrstaProjekta vp = (VrstaProjekta) jcbVrstaProjekta.getSelectedItem();

            TransferObjekatZahtev toz1 = new TransferObjekatZahtev();
            toz1.setOperacija(Konst.VRATI_PRIMARNI_KLJUC_PROJEKAT);
            Komunikacija.getInstance().posaljiZahtev(toz1);
            TransferObjekatOdgovor too1 = Komunikacija.getInstance().primiOdgovor();
            int pkP = (int) too1.getRezultat();

            Projekat p = new Projekat(pkP, naziv, datumOd, datumDo, vp);

            AngazovanjeTableModel model = (AngazovanjeTableModel) jtblAngazovanje.getModel();
            List<Angazovanje> listaA = model.vratiListu();

            TransferObjekatZahtev toz2 = new TransferObjekatZahtev();
            toz2.setOperacija(Konst.VRATI_PRIMARNI_KLJUC_ANGAZOVANJE);
            Komunikacija.getInstance().posaljiZahtev(toz2);
            TransferObjekatOdgovor too2 = Komunikacija.getInstance().primiOdgovor();
            int pkA = (int) too2.getRezultat();

            for (Angazovanje a : listaA) {
                a.setProjekat(p);
                a.setAngazovanjeID(pkA);
                pkA++;
                System.out.println(a.getAngazovanjeID() + ", " + a.getInzenjer().toString() + ", " + a.getProjekat().getProjekatID() + ", " + a.getDatumAngazovanja() + "\n");
            }
            p.setLista(listaA);
            System.out.println(p.getProjekatID() + ", " + p.getNaziv() + ", " + p.getDatumOd() + ", " + p.getDatumDo() + ", " + p.getVrsta().getVrstaID());
            TransferObjekatZahtev toz3 = new TransferObjekatZahtev();
            toz3.setOperacija(Konst.SACUVAJ_PROJEKAT);
            toz3.setParametar(p);
            Komunikacija.getInstance().posaljiZahtev(toz3);
            TransferObjekatOdgovor too3 = Komunikacija.getInstance().primiOdgovor();
            if (too3.getIzuzetak() != null) {
                JOptionPane.showMessageDialog(this, "Uspesno cuvanje projekta i odgovarajucih angazovanja!");
            }
            
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Neuspesno cuvanje projekta i odgovarajucih angazovanja!");
        }
    }//GEN-LAST:event_jbtnSacuvajActionPerformed

