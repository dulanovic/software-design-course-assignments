    private boolean izvrsiValidaciju() {
        if (jtxtNaziv.equals("")) {
            JOptionPane.showMessageDialog(this, "Niste uneli naziv projekta!");
            return false;
        }
        if (jtxtDatumOd.equals("")) {
            JOptionPane.showMessageDialog(this, "Niste uneli datum pocetka projekta!");
            return false;
        }
        if (jtxtDatumDo.equals("")) {
            JOptionPane.showMessageDialog(this, "Niste uneli datum zavrsetka projekta!");
            return false;
        }
        AngazovanjeTableModel model = (AngazovanjeTableModel) jtblAngazovanje.getModel();
        List<Angazovanje> lista = model.vratiListu();

        if (lista.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Niste uneli nijedno angazovanje!");
            return false;
        }

        for (Angazovanje a : lista) {
            if (a.getInzenjer().getInzenjerID() == 0) {
                JOptionPane.showMessageDialog(this, "Niste uneli inzenjera kod angazovanja!");
                return false;
            }
            if (a.getDatumAngazovanja() == null) {
                JOptionPane.showMessageDialog(this, "Niste uneli datum kod angazovanja!");
                return false;
            }
        }

        return true;
    }
