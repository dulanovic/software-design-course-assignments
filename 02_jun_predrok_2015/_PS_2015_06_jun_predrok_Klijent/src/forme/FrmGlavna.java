    private int vratiKljuc() throws IOException, ClassNotFoundException {
        TransferObjekatZahtev toz = new TransferObjekatZahtev();
        toz.setOperacija(Konstante.VRATI_KLJUC);
        Komunikacija.getInstance().posaljiZahtev(toz);
        TransferObjekatOdgovor too = Komunikacija.getInstance().primiOdgovor();
        int kljuc = (int) too.getRezultat();
        System.out.println(kljuc);
        return kljuc;
    }

    private boolean izvrsiValidaciju() {
        GlasanjeTableModel model = (GlasanjeTableModel) jtblGlasanje.getModel();
        List<Glasanje> lista = model.vratiListu();
        int broj1 = 0;
        int broj3 = 0;
        int broj5 = 0;

        for (Glasanje g : lista) {
            if (g.getOdDrzava().equals(g.getZaDrzava())) {
                JOptionPane.showMessageDialog(this, "Glas za samog sebe!");
                return false;
            }
        }

        if (lista.size() != 3) {
            JOptionPane.showMessageDialog(this, "Niste poslali sve glasove!");
            return false;
        }

        for (Glasanje g : lista) {
            if (g.getBrojPoena() == 1) {
                broj1++;
            }
            if (g.getBrojPoena() == 3) {
                broj3++;
            }
            if (g.getBrojPoena() == 5) {
                broj5++;
            }
        }
        if (!(broj1 == 1 && broj3 == 1 && broj5 == 1)) {
            JOptionPane.showMessageDialog(this, "Niste propisno uneli glasove!");
//                broj1 = 0;
//                broj3 = 0;
//                broj5 = 0;
            return false;
        }
        Drzava d = lista.get(0).getOdDrzava();
        for (Glasanje g : lista) {
            if (!d.equals(g.getOdDrzava())) {
                JOptionPane.showMessageDialog(this, "Nije isti glasac!");
                return false;
            }
        }

        return true;
    }
