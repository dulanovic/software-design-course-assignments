    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        try {
            if (!izvrsiValidaciju()) {
                return;
            }

            int kljuc = vratiKljuc();

            GlasanjeTableModel model = (GlasanjeTableModel) jtblGlasanje.getModel();
            List<Glasanje> lista = model.vratiListu();

            for (Glasanje g : lista) {
                g.setGlasanjeID(kljuc);
                kljuc++;
            }

            for (Glasanje g : lista) {
                System.out.println(g.getGlasanjeID() + ", " + g.getOdDrzava() + ", " + g.getZaDrzava() + ", " + g.getBrojPoena());
            }

            TransferObjekatZahtev toz = new TransferObjekatZahtev();
            toz.setOperacija(Konstante.SACUVAJ_GLASANJA);
            toz.setParametar(lista);
            Komunikacija.getInstance().posaljiZahtev(toz);

            JOptionPane.showMessageDialog(this, "Rezultati glasanja su uspesno sacuvani!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getStackTrace());
        }
    }//GEN-LAST:event_jbtnSacuvajActionPerformed

