    private void srediFormu() {
        try {
            List<Drzava> listaDrzava = new ArrayList<>();
            TransferObjekatZahtev toz = new TransferObjekatZahtev();
            toz.setOperacija(Konstante.VRATI_DRZAVE);
            Komunikacija.getInstance().posaljiZahtev(toz);
            TransferObjekatOdgovor too = Komunikacija.getInstance().primiOdgovor();
            if (too.getIzuzetak() != null) {
                throw (Exception) too.getIzuzetak();
            }

            listaDrzava = (List<Drzava>) too.getRezultat();

            jcbOdDrzava.setModel(new DefaultComboBoxModel(listaDrzava.toArray()));
            jcbZaDrzava.setModel(new DefaultComboBoxModel(listaDrzava.toArray()));

            GlasanjeTableModel model = new GlasanjeTableModel(new ArrayList<Glasanje>());
            jtblGlasanje.setModel(model);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

