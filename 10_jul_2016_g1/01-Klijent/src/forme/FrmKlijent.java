    private void srediFormu() {
        try {
            this.setTitle("[FON] Euro 2016 - klijent");

            this.listaZaIzmenu = new ArrayList<>();
            this.listaZaBrisanje = new ArrayList<>();

            TransferObjekatZahtev toz1 = new TransferObjekatZahtev();
            toz1.setOperacija(Konst.VRATI_UTAKMICE);
            Komunikacija.getInstance().posaljiZahtev(toz1);
            TransferObjekatOdgovor too1 = Komunikacija.getInstance().primiOdgovor();
            List<Utakmica> listaU = (List<Utakmica>) too1.getRezultat();

            UtakmicaTableModel model = new UtakmicaTableModel(listaU);
            jtblUtakmice.setModel(model);

            JComboBox jcbDomacin = new JComboBox();
            JComboBox jcbGost = new JComboBox();

            TransferObjekatZahtev toz2 = new TransferObjekatZahtev();
            toz2.setOperacija(Konst.VRATI_REPREZENTACIJE);
            Komunikacija.getInstance().posaljiZahtev(toz2);
            TransferObjekatOdgovor too2 = Komunikacija.getInstance().primiOdgovor();
            List<Reprezentacija> listaR = (List<Reprezentacija>) too2.getRezultat();

            TableColumn tcDomacin = jtblUtakmice.getColumnModel().getColumn(1);
            TableColumn tcGost = jtblUtakmice.getColumnModel().getColumn(2);
            tcDomacin.setCellEditor(new DefaultCellEditor(jcbDomacin));
            tcGost.setCellEditor(new DefaultCellEditor(jcbGost));

            jcbDomacin.setModel(new DefaultComboBoxModel(listaR.toArray()));
            jcbGost.setModel(new DefaultComboBoxModel(listaR.toArray()));
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Neuspesna inicijalizacija forme i njenih komponenti!");
        }
    }

