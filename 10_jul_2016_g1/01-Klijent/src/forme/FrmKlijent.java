    private void jbtnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnIzmeniActionPerformed
        int red = jtblUtakmice.getSelectedRow();
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Niste odabrali red u tabeli!");
        }
        UtakmicaTableModel model = (UtakmicaTableModel) jtblUtakmice.getModel();
        Utakmica u = model.vratiUtakmicu(red);
        listaZaIzmenu.add(u);
    }//GEN-LAST:event_jbtnIzmeniActionPerformed

