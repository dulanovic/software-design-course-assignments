    private void srediFormu() {
        try {
            KonfiguracijaTableModel model = new KonfiguracijaTableModel(new Konfiguracija());
            jtblStavke.setModel(model);

            TransferObjekatZahtev toz1 = new TransferObjekatZahtev();
            toz1.setOperacija(Konstante.VRATI_BRENDOVE);
            Komunikacija.getInstance().posaljiZahtev(toz1);
            TransferObjekatOdgovor too1 = Komunikacija.getInstance().primiOdgovor();
            List<Brend> listaBrendova = (List<Brend>) too1.getRezultat();
            jcbBrend.setModel(new DefaultComboBoxModel(listaBrendova.toArray()));

            TransferObjekatZahtev toz2 = new TransferObjekatZahtev();
            toz2.setOperacija(Konstante.VRATI_KOMPONENTE);
            Komunikacija.getInstance().posaljiZahtev(toz2);
            TransferObjekatOdgovor too2 = Komunikacija.getInstance().primiOdgovor();
            List<Komponenta> listaKomponenti = (List<Komponenta>) too2.getRezultat();

            JComboBox jcbKomponenta = new JComboBox();
            jcbKomponenta.setModel(new DefaultComboBoxModel(listaKomponenti.toArray()));
            TableColumn tcKomponenta = jtblStavke.getColumnModel().getColumn(0);
            tcKomponenta.setCellEditor(new DefaultCellEditor(jcbKomponenta));

        } catch (Exception ex) {
            Logger.getLogger(FrmGlavna.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
