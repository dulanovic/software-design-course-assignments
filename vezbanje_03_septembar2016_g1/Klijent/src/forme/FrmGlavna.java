    private void jbtnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnObrisiActionPerformed
        int red = jtblStavke.getSelectedRow();
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Morate odabrati stavku konfiguracije!", "Sistemska poruka", JOptionPane.WARNING_MESSAGE);
        }

        KonfiguracijaTableModel model = (KonfiguracijaTableModel) jtblStavke.getModel();
        model.obrisiStavku(red);
    }//GEN-LAST:event_jbtnObrisiActionPerformed

    private void jbtnGenerisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGenerisiActionPerformed
        try {
            TransferObjekatZahtev toz = new TransferObjekatZahtev();
            toz.setOperacija(Konstante.VRATI_ID);
            Komunikacija.getInstance().posaljiZahtev(toz);
            TransferObjekatOdgovor too = Komunikacija.getInstance().primiOdgovor();
            int id = (int) too.getRezultat();
            jtxtKonfiguracijaID.setText(String.valueOf(id));
        } catch (Exception ex) {
            Logger.getLogger(FrmGlavna.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jbtnGenerisiActionPerformed

    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        try {
            int konfiguracijaID = Integer.parseInt(jtxtKonfiguracijaID.getText());
            String naziv = jtxtNaziv.getText();
            Date datumIzrade = df.parse(jtxtDatumIzrade.getText());
            Brend brend = (Brend) jcbBrend.getSelectedItem();

            KonfiguracijaTableModel model = (KonfiguracijaTableModel) jtblStavke.getModel();
            Konfiguracija k = model.getK();
            k.setKonfiguracijaID(konfiguracijaID);
            k.setNaziv(naziv);
            k.setDatumIzrade(datumIzrade);
            k.setBrend(brend);
            
            TransferObjekatZahtev toz = new TransferObjekatZahtev();
            toz.setOperacija(Konstante.SACUVAJ_KONFIGURACIJU);
            toz.setParametar(k);
            Komunikacija.getInstance().posaljiZahtev(toz);
        } catch (Exception ex) {
            Logger.getLogger(FrmGlavna.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbtnSacuvajActionPerformed

