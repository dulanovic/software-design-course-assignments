    private boolean izvrsiValidaciju() {
        int brojMaticnihPloca = 0;
        int brojProcesora = 0;
        int brojMemorjiskihModula = 0;

        StavkaTableModel model = (StavkaTableModel) jtblStavke.getModel();
        List<StavkaKonfiguracije> lista = model.vratiListu();

        for (StavkaKonfiguracije sk : lista) {
            if (sk.getKomponenta().getNaziv().equals("Maticna ploca")) {
                brojMaticnihPloca += sk.getKolicina();
            }
            if (sk.getKomponenta().getNaziv().equals("Procesor")) {
                brojProcesora += sk.getKolicina();
            }
            if (sk.getKomponenta().getNaziv().equals("Memorija")) {
                brojMemorjiskihModula += sk.getKolicina();
            }
        }

        if (brojMaticnihPloca != 1 || brojProcesora != 1 || (brojMemorjiskihModula < 1 || brojMemorjiskihModula > 4)) {
            JOptionPane.showMessageDialog(this, "Niste uneli propisnu konfiguraciju!");
            System.out.println("Broj maticnih: " + brojMaticnihPloca + "\nBrojprocesora: " + brojProcesora + "\nBroj memorijskih modula: " + brojMemorjiskihModula);
            return false;
        }
        System.out.println("Broj maticnih: " + brojMaticnihPloca + "\nBrojprocesora: " + brojProcesora + "\nBroj memorijskih modula: " + brojMemorjiskihModula);
        return true;
    }
