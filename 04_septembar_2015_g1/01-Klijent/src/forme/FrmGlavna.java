    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        try {
            if (!izvrsiValidaciju()) {
                return;
            }

            int konfiguracijaID = Integer.parseInt(jtxtKonfiguracijaID.getText());
            String naziv = jtxtNaziv.getText();
            Date datumIzrade = df.parse(jtxtDatumIzrade.getText());
            Brend b = (Brend) jcbBrend.getSelectedItem();

            Konfiguracija k = new Konfiguracija(konfiguracijaID, naziv, datumIzrade, b);

            StavkaTableModel model = (StavkaTableModel) jtblStavke.getModel();
            k.setListaStavki(model.vratiListu());

            TransferObjekatZahtev toz = new TransferObjekatZahtev();
            toz.setOperacija(Konst.SACUVAJ_KONFIGURACIJU);
            toz.setParametar(k);
            Komunikacija.getInstance().posaljiZahtev(toz);

            JOptionPane.showMessageDialog(this, "Uspesno cuvanje konfiguracije!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Neuspesno cuvanje konfiguracije!");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jbtnSacuvajActionPerformed

