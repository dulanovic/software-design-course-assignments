    private void srediFormu() {
        try {
            this.setTitle("Univerzitet u Beogradu - Prijemni 2015.");

            PrijemniIspitTableModel model = new PrijemniIspitTableModel(new ArrayList<>());
            jtblPrijemniIspit.setModel(model);

            TransferObjekatZahtev toz = new TransferObjekatZahtev();
            toz.setOperacija(Konst.VRATI_STUDIJSKE_PROGRAME);
            Komunikacija.getInstance().posaljiZahtev(toz);
            TransferObjekatOdgovor too = Komunikacija.getInstance().primiOdgovor();
            List<StudijskiProgram> listaStudijskihPrograma = (List<StudijskiProgram>) too.getRezultat();

            TransferObjekatZahtev toz1 = new TransferObjekatZahtev();
            toz1.setOperacija(Konst.VRATI_KANDIDATE);
            Komunikacija.getInstance().posaljiZahtev(toz1);
            TransferObjekatOdgovor too1 = Komunikacija.getInstance().primiOdgovor();
            List<Kandidat> listaKandidata = (List<Kandidat>) too1.getRezultat();

            jcbStudijskiProgram.setModel(new DefaultComboBoxModel(listaStudijskihPrograma.toArray()));
            jcbKandidat.setModel(new DefaultComboBoxModel(listaKandidata.toArray()));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Neuspesno ucitavanje listi na formu!");
        }
    }

