    private boolean izvrsiValidaciju() {
        if (jtxtBrojPoena.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Niste uneli broj poena!");
            return false;
        }

        double brojPoena;
        try {
            brojPoena = Double.parseDouble(jtxtBrojPoena.getText());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Niste uneli broj kao broj poena!");
            return false;
        }
        if (brojPoena > 100 || brojPoena < 0) {
            JOptionPane.showMessageDialog(this, "Uneti broj poena nije u predvidjenom rasponu: [0-100]!");
            return false;
        }
        return true;
    }

    private boolean izvrsiValidacijuListe() {
        PrijemniIspitTableModel model = (PrijemniIspitTableModel) jtblPrijemniIspit.getModel();
        List<PrijemniIspit> lista = model.vratiListu();

        for (PrijemniIspit pi : lista) {
            int brojParova = 0;
            Kandidat k = pi.getKandidat();
            StudijskiProgram sp = pi.getStudijskiProgram();

            for (PrijemniIspit pi1 : lista) {
                if (pi1.getStudijskiProgram() == sp && pi1.getKandidat() == k) {
                    brojParova++;
                }
            }
            System.out.println("metoda za validaciju liste: " + brojParova);
            if (brojParova > 1) {
                return false;
            }
        }

        return true;
    }
