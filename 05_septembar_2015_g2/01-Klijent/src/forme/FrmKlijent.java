    private void jbtnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDodajActionPerformed
        if (!izvrsiValidaciju()) {
            return;
        }

        StudijskiProgram sp = (StudijskiProgram) jcbStudijskiProgram.getSelectedItem();
        Kandidat k = (Kandidat) jcbKandidat.getSelectedItem();
        double brojPoena = Double.parseDouble(jtxtBrojPoena.getText());

        PrijemniIspit pi = new PrijemniIspit(sp, k, brojPoena);

        PrijemniIspitTableModel model = (PrijemniIspitTableModel) jtblPrijemniIspit.getModel();
        model.dodajRed(pi);
    }//GEN-LAST:event_jbtnDodajActionPerformed

    private void jbtnUkloniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnUkloniActionPerformed
        int red = jtblPrijemniIspit.getSelectedRow();
        if (red == -1) {
            JOptionPane.showMessageDialog(this, "Niste odabrali red!");
            return;
        }
        PrijemniIspitTableModel model = (PrijemniIspitTableModel) jtblPrijemniIspit.getModel();
        model.obrisiRed(red);
    }//GEN-LAST:event_jbtnUkloniActionPerformed

    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        try {
            if (!izvrsiValidacijuListe()) {
                System.out.println("true/false -> " + izvrsiValidacijuListe());
                JOptionPane.showMessageDialog(this, "Isti kandidat je konkurisao za isti studijski program dvaput!");
                return;
            }

            PrijemniIspitTableModel model = (PrijemniIspitTableModel) jtblPrijemniIspit.getModel();
            List<PrijemniIspit> lista = model.vratiListu();

            for (PrijemniIspit pi : lista) {
                System.out.println(pi.getKandidat() + ", " + pi.getStudijskiProgram());
            }

            TransferObjekatZahtev toz = new TransferObjekatZahtev();
            toz.setOperacija(Konst.SACUVAJ_PRIJEMNI_ISPIT);
            toz.setParametar(lista);
            Komunikacija.getInstance().posaljiZahtev(toz);

            JOptionPane.showMessageDialog(this, "Uspesno sacuvana lista prijemnih ispita!");
        } catch (IOException ioex) {
            JOptionPane.showMessageDialog(this, "Neuspesno sacuvana lista prijemnih ispita!");
            ioex.printStackTrace();
        }
    }//GEN-LAST:event_jbtnSacuvajActionPerformed

